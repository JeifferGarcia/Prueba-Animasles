import Random from "./Random.js";

let aleatorio = Random(0, 7);
const Animals = () => {
  const animales = [
    "burro",
    "caballo",
    "perro",
    "vaca",
    "gallina",
    "cerdo",
    "conejo",
    "cabra",
  ];

  if (aleatorio === aleatorio) {
    Random(0, 7);
  }

  return animales[aleatorio];
};

export default Animals;
